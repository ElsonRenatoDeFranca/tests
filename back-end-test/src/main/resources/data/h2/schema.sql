DROP TABLE IF EXISTS PUBLIC.PRODUCT;
DROP TABLE IF EXISTS PUBLIC.CATEGORY;
DROP TABLE IF EXISTS PUBLIC.PRODUCT_CATEGORY;

CREATE SEQUENCE IF NOT EXISTS PUBLIC.PRODUCT_SEQ;
CREATE SEQUENCE IF NOT EXISTS PUBLIC.CATEGORY_SEQ;

CREATE TABLE PUBLIC.PRODUCT(
    ID BIGINT DEFAULT PRODUCT_SEQ.nextval,
    PRODUCT_ID BIGINT,
    PRODUCT_NAME VARCHAR(50),
    PRODUCT_DESCRIPTION VARCHAR(80),
    PRODUCT_COST VARCHAR(30),
    PRIMARY KEY (PRODUCT_ID)
);

CREATE TABLE PUBLIC.CATEGORY(
    ID BIGINT DEFAULT CATEGORY_SEQ.nextval,
    CATEGORY_ID BIGINT,
    CATEGORY_NAME VARCHAR(50),
    PRIMARY KEY (CATEGORY_ID)
);

CREATE TABLE PRODUCT_CATEGORY (
    CATEGORY_ID BIGINT NOT NULL,
    PRODUCT_ID  BIGINT NOT NULL,
    FOREIGN KEY (CATEGORY_ID) REFERENCES PUBLIC.CATEGORY,
    FOREIGN KEY (PRODUCT_ID) REFERENCES PUBLIC.PRODUCT,
);